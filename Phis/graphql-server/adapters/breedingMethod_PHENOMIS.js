    static readById(iri) {
        let query = `
          query
            readOneBreedingMethod
            {
              readOneBreedingMethod(breedingMethodDbId:"${iri}")
              {
                breedingMethodDbId 
                abbreviation 
                breedingMethodName 
                description 
              }
            }`;

        return axios.post(remoteCenzontleURL, {
            query: query
        }).then(res => {
            //check
            if (res && res.data && res.data.data) {
                return res.data.data.readOneBreedingMethod;
            } else {
                throw new Error(`Invalid response from remote cenz-server: ${remoteCenzontleURL}`);
            }
        }).catch(error => {
            error['url'] = remoteCenzontleURL;
            handleError(error);
        });
    }

